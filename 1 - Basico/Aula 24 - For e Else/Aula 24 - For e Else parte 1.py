"""
    For / Else em python
"""

var1 = ['Lucas Andrade', 'joãozinho', 'Maria']
var2 = ['1', '2', '3']

# printa os valores da lista
for valor in var1:
    print(valor)
    continue
    print(valor)
# A palavra continue faz pular a ação anterior, e resolvendo a proxima

for valor in var2:
    print(valor)
    break
# A palavra break quebra na hora o código, encerrando.


