#  utilização do IN e NOT IN

nome = 'Lucas Andrade'

if 'u' in nome:
    print("Existe a letra u no seu nome")
#  Se a string 'u' esta dentro da variavel nome então é verdadeiro
else:
    print("Seu nome não possui a letra u")

if not 'u' in nome:
    print("Existe as letras U no seu nome")
#  Se a string 'AND' esta dentro da variavel nome então é verdadeiro
else:
    print("Seu nome não possui a letra u")



